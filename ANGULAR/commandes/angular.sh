# Créer un nouveau projet Angular
ng new nom_projet

# Démarrer le serveur HTTP de développement (démarrer l'application Angular dans le répertoire du projet, à la racine)
# Le serveur HTTP de développement écoute sur le port 4200 par défaut
ng serve
ng s

# Générer une nouvelle classe
ng generate class nom_classe
ng g class nom_classe

# Générer une nouvelle interface
ng generate interface nom_interface
ng g i nom_interface

# Générer une nouvelle directive
ng generate directive nom_directive
ng g d nom_directive

# Générer un nouveau composant
ng generate component nom_composant
ng g c nom_composant

# Générer un nouveau Pipe
ng generate pipe nom_pipe
ng g p nom_pipe