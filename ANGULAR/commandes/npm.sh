# Voir la version
npm -v

# Installer une nouvelle dépendance sur le projet (dans le répertoire du projet, à la racine)
npm install nom_dependance
npm i nom_dependance

# Installer les dépendances manquantes au projet (dans le répertoire du projet)
npm install
npm i

# Installer un script / application de manière globale, sur l'ordinateur
npm install -g nom_app
npm i -g nom_app
